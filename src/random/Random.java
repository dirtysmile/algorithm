package random;

public class Random {
    public static void main(String[] args) {
        // 숫자 랜덤
        System.out.println((int)(Math.random()*10));

        // 문자 랜덤
        System.out.println((char)((int)(Math.random()*26)+65)); // 랜덤 한 대문자
        System.out.println((char)((int)(Math.random()*26)+97)); // 랜덤 한 소문자





    }
}
